            t2 = T0[(s2 shr 24) and 0xff] xor T1[(s3 shr 16) and 0xff] xor T2[(s0 shr 8) and 0xff] xor T3[s1 and 0xff] xor roundKeys[rk+2]
            t3 = T0[(s3 shr 24) and 0xff] xor T1[(s0 shr 16) and 0xff] xor T2[(s1 shr 8) and 0xff] xor T3[s2 and 0xff] xor roundKeys[rk+3]
            s0 = t0; s1 = t1; s2 = t2; s3 = t3
