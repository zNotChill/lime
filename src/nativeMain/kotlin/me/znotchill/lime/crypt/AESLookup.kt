    val RCON = intArrayOf(
        0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
    )

    val T0 = IntArray(256) { i ->
        val s = SBOX[i]
        val s2 = if (s and 0x80 != 0) (s shl 1) xor 0x1b else s shl 1

        ((s2 and 0xff) shl 24) or
                ((s and 0xff) shl 16) or
                ((s and 0xff) shl 8) or
                ((s2 xor s) and 0xff)
    }
    val T1 = IntArray(256) { i -> T0[i].rotateRight(8) }
    val T2 = IntArray(256) { i -> T0[i].rotateRight(16) }
    val T3 = IntArray(256) { i -> T0[i].rotateRight(24) }

    fun expandKey(key: ByteArray): IntArray {
        val w = IntArray(44)
        for (i in 0..3) {
            w[i] = ((key[i*4].toInt() and 0xff) shl 24) or
                    ((key[i*4+1].toInt() and 0xff) shl 16) or
                    ((key[i*4+2].toInt() and 0xff) shl 8) or
                    (key[i*4+3].toInt() and 0xff)
        }
        for (i in 4..43) {
            var temp = w[i-1]
            if (i % 4 == 0) {
                temp = ((temp shl 8) or (temp ushr 24))
                temp = ((SBOX[(temp shr 24) and 0xff]) shl 24) or
                        ((SBOX[(temp shr 16) and 0xff]) shl 16) or
                        ((SBOX[(temp shr 8) and 0xff]) shl 8) or
                        (SBOX[temp and 0xff])
                temp = temp xor (RCON[i/4] shl 24)
            }
            w[i] = w[i-4] xor temp
        }
        return w
    }

    fun encryptBlock(roundKeys: IntArray, input: ByteArray, output: ByteArray) {
        var s0 = ((input[0].toInt() and 0xff) shl 24) or ((input[1].toInt() and 0xff) shl 16) or ((input[2].toInt() and 0xff) shl 8) or (input[3].toInt() and 0xff)
        var s1 = ((input[4].toInt() and 0xff) shl 24) or ((input[5].toInt() and 0xff) shl 16) or ((input[6].toInt() and 0xff) shl 8) or (input[7].toInt() and 0xff)
        var s2 = ((input[8].toInt() and 0xff) shl 24) or ((input[9].toInt() and 0xff) shl 16) or ((input[10].toInt() and 0xff) shl 8) or (input[11].toInt() and 0xff)
        var s3 = ((input[12].toInt() and 0xff) shl 24) or ((input[13].toInt() and 0xff) shl 16) or ((input[14].toInt() and 0xff) shl 8) or (input[15].toInt() and 0xff)

        s0 = s0 xor roundKeys[0]; s1 = s1 xor roundKeys[1]
        s2 = s2 xor roundKeys[2]; s3 = s3 xor roundKeys[3]

        var t0: Int; var t1: Int; var t2: Int; var t3: Int
        for (r in 1..9) {
            val rk = r * 4
            t0 = T0[(s0 shr 24) and 0xff] xor T1[(s1 shr 16) and 0xff] xor T2[(s2 shr 8) and 0xff] xor T3[s3 and 0xff] xor roundKeys[rk]
